#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*list;
	t_list	*next_node;

	if (*lst != NULL)
	{
		list = *lst;
		while (list != NULL)
		{
			next_node = list->next;
			if (del != NULL)
				del(list->content);
			free(list);
			list = next_node;
		}
		*lst = NULL;
	}
}
